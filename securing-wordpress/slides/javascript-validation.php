	<h1 class="slide-title" style="text-align:center;">JavaScript Validation (using jQuery)</h1>
	<p>JavaScript validation falls largely into a few utility functions which you will customize for your needs.</p>
	<ul>
		<li><code>jQuery.parseFloat(str)</code> Float value of the string passed in, false if it does not parse.</li>
		<li><code>jQuery.parseInt(str)</code> Integer value of string passed in, false if it does not parse.</li>
		<li><code>$input.val().length</code> The length of the value of $input. 0 means empty.</li>
		<li><code>(string).match(regex)</code> A powerful method to validate a string pattern against a regular expression.</li>
	</ul>
