	<h1 class="slide-title" style="text-align:center;">Server Validation</h1>
	<p>Commonly-used methods for server side validation in PHP and WordPress.</p>
	<ul>
		<li><code>empty($val)</code> True if the value is false, null, undefined, empty string, or 0.</li>
		<li><code>isset($var)</code> True if the $var passed results has a defined value, even if it's empty.</li>
		<li><code>is_numeric($val)</code> True if the string contains only numeric values.</li>
		<li><code>is_email($val)</code> WordPress function. "true" if value is a valid email address.</li>
	</ul>
